/**********************************************************************
 * @file script2.cpp
 * @brief Перемещение всех нулей в конец вектора с сохранением порядка остальных элементов.
 *
 * @details Считывает последовательность целых чисел, перемещает все нули
 * в конец вектора, сохраняя относительный порядок ненулевых элементов.
 * Использует один вектор без создания дополнительных структур данных.
 *
 * @date 2025-12-01
 * @copyright Copyright (c) 2025
 **********************************************************************/

/********** Core **********/
#include <iostream>
#include <vector>

/********** Main Function **********/
/**
 * @brief Точка входа программы.
 *
 * @return 0 при успешном выполнении
 */
int main(void)
{
    std::vector<int> nums;
    int value;

    // Считываем все числа из входного потока
    while (std::cin >> value)
    {
        nums.push_back(value);
    }

    // Используем два указателя: один для чтения, другой для записи
    // writePos указывает на позицию, куда нужно записать следующий ненулевой элемент
    size_t writePos = 0u;

    // Проходим по всем элементам и перемещаем ненулевые элементы в начало
    for (size_t readPos = 0u; readPos < nums.size(); ++readPos)
    {
        if (nums[readPos] != 0)
        {
            nums[writePos] = nums[readPos];
            ++writePos;
        }
    }

    // Заполняем оставшуюся часть нулями
    for (size_t i = writePos; i < nums.size(); ++i)
    {
        nums[i] = 0;
    }

    // Выводим результат
    for (size_t i = 0u; i < nums.size(); ++i)
    {
        if (i > 0u)
        {
            std::cout << " ";
        }
        std::cout << nums[i];
    }

    if (!nums.empty())
    {
        std::cout << " " << std::endl;
    }

    return 0;
}
